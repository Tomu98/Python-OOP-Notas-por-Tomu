# --- QUE SON LOS PATRONES DE DISEÑO ---

# Los patrones de diseño, en el contexto de POO, son soluciones probadas y generalmente aceptadas para problemas comunes que enfrentan los programadores al diseñar software.
# Estos patrones proporcionan un enfoque estructurado y reutilizable para resolver problemas específicos de diseño de software de manera eficiente.

# Los patrones de diseño ayudan a los desarrolladores a:
# - Abstraer complejidad: Simplifican la resolución de problemas al proporcionar una estructura organizada para el código.
# - Fomentar la reutilización de código: Los patrones de diseño promueven la reutilización al definir soluciones probadas que pueden aplicarse a diferentes situaciones.
# - Facilitar la comunicación: Los patrones de diseño tienen nombres y conceptos comunes que permiten a los desarrolladores comunicarse de manera efectiva sobre la estructura de sus diseños.
# - Mejorar el mantenimiento: Al seguir patrones de diseño, el código tiende a ser más mantenible y menos propenso a errores.

# Existen varios patrones de diseño, que se dividen en categorías según su propósito. Algunas categorías comunes de patrones de diseño incluyen:

# Patrones de creación:
# Estos patrones se utilizan para instanciar objetos de manera flexible y eficiente.
# Ejemplos incluyen el patrón Singleton, el patrón Factory Method y el patrón Builder.

# Patrones de estructura:
# Estos patrones se centran en la composición de clases y objetos para formar estructuras más complejas.
# Ejemplos incluyen el patrón Adapter, el patrón Composite y el patrón Bridge.

# Patrones de comportamiento:
# Estos patrones se ocupan de cómo los objetos interactúan y comunican entre sí.
# Ejemplos incluyen el patrón Observer, el patrón Strategy y el patrón Command.

# Patrones arquitectónicos:
# Estos patrones son utilizados para diseñar la estructura general de una aplicación o sistema.
# Ejemplos incluyen el patrón Modelo-Vista-Controlador (MVC) y el patrón Modelo-Vista-ViewModel (MVVM).


# Al aprender sobre patrones de diseño, es importante comprender cuándo y cómo aplicar cada uno de ellos en función de las necesidades de tu proyecto.
# No todos los patrones se aplican en todas las situaciones, y elegir el patrón correcto para un problema específico es una habilidad importante para un diseñador de software.
